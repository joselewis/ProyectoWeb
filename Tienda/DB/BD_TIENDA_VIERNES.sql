CREATE TABLE PRODUCTO_ROPA
(
	ID_PRODUCTO INT IDENTITY NOT NULL,
	CODIGO_PRODUCTO INT PRIMARY KEY NOT NULL,
	TIPO_PRENDA VARCHAR(100) NOT NULL,
	PRECIO_PRODUCTO INT NOT NULL,
